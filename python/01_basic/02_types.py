#!/usr/bin/env python3
## 02 - Types

# Integer
age = 30
print("Edad (int):", age, type(age))

# Float
height = 5.9
print("Altura (float):", height, type(height))

# Complex
complex_number = 2 + 3j
print("NÃºmero complejo (complex):", complex_number, type(complex_number))

# String
name = "CodeCraftAvila"
print("Nombre (str):", name, type(name))

# Boolean
is_student = True
print("Estudiante (bool):", is_student, type(is_student))

# NoneType
nothing = None
print("Nada (NoneType):", nothing, type(nothing))

# List
fruits = ["manzana", "banana", "cereza"]
print("Frutas (list):", fruits, type(fruits))

# Tuple
coordinates = (10.0, 20.0)
print("Coordenadas (tuple):", coordinates, type(coordinates))

# dictionary
person = {"nombre": "Ana", "edad": 28}
print("Persona (dict):", person, type(person))

# Range
range_example = range(5)
print("Rango (range):", list(range_example), type(range_example))

# Set
set_example = {1, 2, 3, 4, 5}
print("Conjunto (set):", set_example, type(set_example))
